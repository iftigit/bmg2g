package org.table;

public class FingerDTO {
	private String regno;
	private byte [] LT;
	private byte [] LI;
	private byte [] LM;
	private byte [] LR;
	private byte [] LL;
	
	private byte [] RT;
	private byte [] RI;
	private byte [] RM;
	private byte [] RR;
	private byte [] RL;
	
	public String getRegno() {
		return regno;
	}
	public void setRegno(String regno) {
		this.regno = regno;
	}
	public byte[] getLT() {
		return LT;
	}
	public void setLT(byte[] lt) {
		LT = lt;
	}
	public byte[] getLI() {
		return LI;
	}
	public void setLI(byte[] li) {
		LI = li;
	}
	public byte[] getLM() {
		return LM;
	}
	public void setLM(byte[] lm) {
		LM = lm;
	}
	public byte[] getLR() {
		return LR;
	}
	public void setLR(byte[] lr) {
		LR = lr;
	}
	public byte[] getLL() {
		return LL;
	}
	public void setLL(byte[] ll) {
		LL = ll;
	}
	public byte[] getRT() {
		return RT;
	}
	public void setRT(byte[] rt) {
		RT = rt;
	}
	public byte[] getRI() {
		return RI;
	}
	public void setRI(byte[] ri) {
		RI = ri;
	}
	public byte[] getRM() {
		return RM;
	}
	public void setRM(byte[] rm) {
		RM = rm;
	}
	public byte[] getRR() {
		return RR;
	}
	public void setRR(byte[] rr) {
		RR = rr;
	}
	public byte[] getRL() {
		return RL;
	}
	public void setRL(byte[] rl) {
		RL = rl;
	}
	
	
	

}
