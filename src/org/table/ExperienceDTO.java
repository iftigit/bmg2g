package org.table;

public class ExperienceDTO {
	
	private String companyName="";
	private String position="";
	private String serveFrom="";
	private String serveTill="";
	private String address="";
	
	public String getCompanyName() {
		return companyName;
	}
	public void setCompanyName(String companyName) {
		this.companyName = companyName;
	}
	public String getPosition() {
		return position;
	}
	public void setPosition(String position) {
		this.position = position;
	}
	public String getServeFrom() {
		return serveFrom;
	}
	public void setServeFrom(String serveFrom) {
		this.serveFrom = serveFrom;
	}
	public String getServeTill() {
		return serveTill;
	}
	public void setServeTill(String serveTill) {
		this.serveTill = serveTill;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	

}
